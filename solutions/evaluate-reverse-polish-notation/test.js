const { evalRPN } = require("./solution.js");

test(`["2","1","+","3","*"]`, () => {
  expect(evalRPN(["2","1","+","3","*"])).toEqual(9);
});

test(`["4","13","5","/","+"]`, () => {
  expect(evalRPN(["4","13","5","/","+"])).toEqual(6);
});

test(`["10","6","9","3","+","-11","*","/","*","17","+","5","+"]`, () => {
  expect(evalRPN(["10","6","9","3","+","-11","*","/","*","17","+","5","+"])).toEqual(22);
});

