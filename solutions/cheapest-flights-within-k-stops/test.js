const { findCheapestPrice } = require("./solution.js");

test(`4,[[0,1,100],[1,2,100],[2,0,100],[1,3,600],[2,3,200]],0,3,1`, () => {
  expect(
    findCheapestPrice(
      4,
      [
        [0, 1, 100],
        [1, 2, 100],
        [2, 0, 100],
        [1, 3, 600],
        [2, 3, 200],
      ],
      0,
      3,
      1
    )
  ).toEqual(700);
});

test(`3,[[0,1,100],[1,2,100],[0,2,500]],0,2,1`, () => {
  expect(
    findCheapestPrice(
      3,
      [
        [0, 1, 100],
        [1, 2, 100],
        [0, 2, 500],
      ],
      0,
      2,
      1
    )
  ).toEqual(200);
});

test(`3,[[0,1,100],[1,2,100],[0,2,500]],0,2,0`, () => {
  expect(
    findCheapestPrice(
      3,
      [
        [0, 1, 100],
        [1, 2, 100],
        [0, 2, 500],
      ],
      0,
      2,
      0
    )
  ).toEqual(500);
});
